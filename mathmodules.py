#### math.ceil() ####

import math
x=12.8
print(math.ceil(x))         # Output=13

#### math.floor() ####
x=10.5
print(math.floor(x))         # Output=10

#### math.fabs() ####
x=-10
print(math.fabs(x))         # Output=10

#### math.factorial() ####
x=5
print(math.factorial(x))    # Output=120

#### math.fsum() ####
l=[10,20,30,40]
print(math.fsum(l))        # Output=100.0

#### math.sqrt() ####
x=25
print(math.sqrt(x))        # Output=5.0
